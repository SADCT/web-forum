package model;

public class Peiwan {
    private int  peiwanId;
    private String peiwanName;
    private String peiwanPassword;
    private String peiwanDesc;
    private int peiwanLevel;
    public int getPeiwanId() {
        return peiwanId;
    }

    public void setPeiwanId(int peiwanId) {
        this.peiwanId = peiwanId;
    }

    public String getPeiwanName() {
        return peiwanName;
    }

    public void setPeiwanName(String peiwanName) {
        this.peiwanName = peiwanName;
    }

    public String getPeiwanPassword() {
        return peiwanPassword;
    }

    public void setPeiwanPassword(String peiwanPassword) {
        this.peiwanPassword = peiwanPassword;
    }

    public String getPeiwanDesc() {
        return peiwanDesc;
    }

    public void setPeiwanDesc(String peiwanDesc) {
        this.peiwanDesc = peiwanDesc;
    }

    public int getPeiwanLevel() {
        return peiwanLevel;
    }

    public void setPeiwanLevel(int peiwanLevel) {
        this.peiwanLevel = peiwanLevel;
    }



}
